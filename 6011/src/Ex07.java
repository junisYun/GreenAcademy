
public class Ex07 {
	public static void main(String[] args) {
		//자바에서 1증가 시키는 방법
		int i = 10;
		System.out.println(i);
		
		i++;
		System.out.println(i);
		
		++i;
		System.out.println(i);
		
		i = i + 1;
		System.out.println(i);
		
		i += 1;
		System.out.println(i);
		
		i = 10;
		i *= 3 + 7;
		System.out.println(i);
	}
}
